package com.course.syntax;

public class StepFirst {

    public static void main(String[] args) {

        byte byteType = -128;
        short shortType = 3000;
        int intType = 2023412442;
        long longType = -1234121324232131233L;
        float floatType = 1.4012985E-12F;
        double doubleType = 4.9406564584124654E-304;
        char charType = 'c';
        boolean booleanType = true;

        System.out.println("All Java types:");
        System.out.println(byteType + " " + shortType + " " + intType + " " + longType + " " + floatType +
                           " " + doubleType + " " + charType + " " + booleanType);
    }

}
